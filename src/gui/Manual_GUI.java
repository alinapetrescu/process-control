    private void cbChoiceLocoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbChoiceLocoActionPerformed
        //modification
        if(cbChoiceLoco.getSelectedItem()==null) return;
        if(!cbChoiceLoco.getSelectedItem().toString().equals("Choose Locomotive")){
            locomotive = railway.getLocomotiveById(cbChoiceLoco.getSelectedItem().toString());
            //System.out.println(cbChoiceLoco.getSelectedItem());
            jsSpeed.setValue(locomotive.getDesiredSpeed());
        }
    }//GEN-LAST:event_cbChoiceLocoActionPerformed

