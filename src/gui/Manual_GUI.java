    private void jsSpeedStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jsSpeedStateChanged
        // TODO add your handling code here: Done
        if(locomotive != null) {
            if(jsSpeed.getValue()>0){
                locomotive.setDirection(0);
                locomotive.reachSpeed(jsSpeed.getValue());
            }else{
                locomotive.setDirection(1);
                locomotive.reachSpeed((jsSpeed.getValue())*(-1));
            }
        }
    }//GEN-LAST:event_jsSpeedStateChanged

